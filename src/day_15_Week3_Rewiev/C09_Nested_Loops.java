package day_15_Week3_Rewiev;

public class C09_Nested_Loops {
    public static void main(String[] args) {
        for(int i=0,j=1;i<5;i++){

            for(int k=1;k<=i;k++){


                System.out.print(j+" ");
                j++;


            }
            System.out.println();

        }
    }
}
