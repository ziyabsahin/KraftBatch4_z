    public static void main(String[] args) {
        int taban=2;
        int tavan=3;

        int resault=1;
        if(tavan==0){
            resault=1;
        } else {

        for(int i=0;i<tavan;i++){
            resault*=taban; }

            System.out.println(resault);
        }

    }
