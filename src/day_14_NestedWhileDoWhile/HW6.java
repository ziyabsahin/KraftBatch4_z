package day_14_NestedWhileDoWhile;

public class HW6 {
    public static void main(String[] args){
        for(int i=15;i<=100;i++){
            System.out.print(i+" ");
        }
        System.out.println(" ");
        int a=15;
        while(a<=100){
            System.out.print(a+" ");
            a++;
        }
        System.out.println(" ");
        int b=15;
        do{
            System.out.print(b+" ");
            b++;

        }while (b<=100);


    }
}
