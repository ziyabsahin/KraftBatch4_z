package day_14_NestedWhileDoWhile;

public class C07_Loop_Practices {
    public static void main(String[] args) {

        for(int i=1; i<=10;i++){
           System.out.print(i+" ");
        }
        System.out.println(" ");

        int y=1;
        while (y<=10){
            System.out.print(y+" ");
            y++;
        }
        System.out.println(" ");
        int k=1;
        do{
            System.out.print(k+" ");
            k++;
        }while(k<=10);


    }

}
