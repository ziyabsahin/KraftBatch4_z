package day_12_Statements;

public class C04_Switch_Case {
    public static void main(String[] args){

        //byte, short, int, String ve char tipindeki degerlerde kullanırız
        int num=1;
        switch (num){
            case 1:
                System.out.println("sayı 1 e esit");
               break;
                case 2:
                System.out.println("sayı 2 ye eşit");
                break;
            default:
                System.out.println("sayı eşleşmedi");





        }












    }
}
